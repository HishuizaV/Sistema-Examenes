    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try {
                if(txtusuario.getText().equals(clsUsuario.compararUsuario(txtusuario.getText()))){
                    objUsuario.setNombre(txtNombres.getText());
                    objUsuario.setApellido(txtApellidos.getText());
                    objUsuario.setTipo((Tipo_Usuario) cmbTipoUsuario.getSelectedItem());
                    objUsuario.setNickname(txtusuario.getText());
                    objUsuario.setContraseña(Password.getText());
                    objUsuario.setCorreo(txtcorreo.getText());
                    objUsuario.setTelefono(txttelefono.getText());
                    objUsuario.registrar();                                     
                }else{
                    JOptionPane.showMessageDialog(null, "Nick de Usuario ya en Uso");
                }
        } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Error al registrar Usuario");
        }
        
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

