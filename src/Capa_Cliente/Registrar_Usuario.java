    public void validar(){
    
        if(txtNombres.getText().isEmpty()){
            validarNobmre.setText("X");
            validarNobmre.setForeground(Color.red);
        }else{
            validarNobmre.setText("Ok");
            validarNobmre.setForeground(Color.green);
        }
        
        if(txtApellidos.getText().isEmpty()){
            ValidarApellido.setText("X");
            ValidarApellido.setForeground(Color.red);
        }else{
            ValidarApellido.setText("Ok");
            ValidarApellido.setForeground(Color.green);
        }
        
        if(txtusuario.getText().isEmpty()){
            validarusuario.setText("X");
            validarusuario.setForeground(Color.red);
        }       
        else{
            validarusuario.setText("Ok");
            validarusuario.setForeground(Color.green);
        }
        
       if(Password.getText().isEmpty()){
            validarcontraseña.setText("X");
            validarcontraseña.setForeground(Color.red);
        }else{
            validarcontraseña.setText("Ok");
            validarcontraseña.setForeground(Color.green);
        }
       
        if(PasswordConfirm.getText().isEmpty()){
            validarconfirmarcontraseña.setText("X");
            validarconfirmarcontraseña.setForeground(Color.red);
        }else if(Password.getText().equals(PasswordConfirm.getText())){
            validarconfirmarcontraseña.setText("Ok");
            validarconfirmarcontraseña.setForeground(Color.green);            
        }else{
            validarconfirmarcontraseña.setText("Incompatible Password");
            validarconfirmarcontraseña.setForeground(Color.red);
        }        
        if(txtcorreo.getText().isEmpty()){
            validarCorreo.setText("X");
            validarCorreo.setForeground(Color.red);
        }else if (!txtcorreo.getText().contains("@") || !txtcorreo.getText().contains(".")){
            validarCorreo.setText("Correo Inválido");
            validarCorreo.setForeground(Color.red);
        }else{
            validarCorreo.setText("Ok");
            validarCorreo.setForeground(Color.green);
        }
        
        if(txttelefono.getText().isEmpty()){
            ValidarTelefono.setText("X");
            ValidarTelefono.setForeground(Color.red);
        }else{
            ValidarTelefono.setText("Ok");
            ValidarTelefono.setForeground(Color.green);
        } 
        
        if(txtNombres.getText().isEmpty()|| txtApellidos.getText().isEmpty() || txtcorreo.getText().isEmpty() || 
                txttelefono.getText().isEmpty()|| txtusuario.getText().isEmpty()|| Password.getText().isEmpty() || 
                PasswordConfirm.getText().isEmpty()){
                btnRegistrar.setEnabled(false);      
        }else{
                btnRegistrar.setEnabled(true);
        }
    }
    
